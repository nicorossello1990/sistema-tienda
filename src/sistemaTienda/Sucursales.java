    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
         if (this.p.bd.agregarSucursal(nombre.getText()) == 0) {
              JOptionPane.showMessageDialog(this,"La sucursal con nombre "+nombre.getText()+ " ya existe.","Advertencia", JOptionPane.WARNING_MESSAGE); //Tipo de mensaje
         }else {
             nombre.setText("");
             agregar.setEnabled(false);
             cargar();     
         };               
    }//GEN-LAST:event_agregarActionPerformed

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        if (tsucursales.getSelectedRow()== -1){
            JOptionPane.showMessageDialog(this,"No ha seleccionado ningún item","Error", JOptionPane.ERROR_MESSAGE); //Tipo de mensaje
        }else{
            model =  (DefaultTableModel) tsucursales.getModel();
            int[] arrayElementos = tsucursales.getSelectedRows();
            for (int i=0; i< arrayElementos.length;i++){             
               String idSucursal =model.getValueAt(arrayElementos[i], 0).toString();
               this.p.bd.eliminarSucursal(idSucursal);
            }        
            cargar();
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

