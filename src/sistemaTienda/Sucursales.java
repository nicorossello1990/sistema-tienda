         private  void cargar(){
                String []Registros=new String[2];
                model= (DefaultTableModel) tsucursales.getModel();
                model = Dominio.eliminarTabla(model); 
                    try {             
                          ResultSet rs = this.p.bd.listarDatosSucursales();
                          while(rs.next())
                          {
                              Registros[0]= rs.getString("id_sucursal");
                              Registros[1]= rs.getString("nombre");                   
                              model.addRow(Registros);
                          }
                          tsucursales.setModel(model);
                    } catch (SQLException ex) {
                        Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
                    }
                cantidad.setText("Cantidad de Sucursales: " + tsucursales.getRowCount());            
  }

