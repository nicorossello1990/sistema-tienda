    private void tclientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tclientesMouseClicked
         int col = tclientes.columnAtPoint(new Point(evt.getX(), evt.getY()));
         if (col==1){
             try {
                 int fila = tclientes.rowAtPoint(new Point(evt.getX(), evt.getY()));
                 String id_cliente =model.getValueAt(fila,0).toString();
                 String nombre =model.getValueAt(fila,1).toString();
                 String tel =model.getValueAt(fila,2).toString();
                 String correo =model.getValueAt(fila,3).toString();
                 FacturasCliente  fc = new FacturasCliente(this.p,id_cliente,nombre,tel,correo);
                 this.p.escritorio.add(fc);
                 this.p.escritorio.setSelectedFrame(fc);
                 fc.show();
                 fc.setMaximum(true);   
                 tclientes.setModel(model);
             } catch (PropertyVetoException ex) {
                 Logger.getLogger(IngresoMisClientes.class.getName()).log(Level.SEVERE, null, ex);
             }                      
          }    
         
       if (evt.getClickCount() == 2){
            modificarCliente();
        } 
  
    }//GEN-LAST:event_tclientesMouseClicked

