     private  void cargar(String valor){
     tclientes.getColumnModel().getColumn(1).setCellRenderer(new TableCellRenderer() {
        @Override
        public Component getTableCellRendererComponent(JTable table, final Object value, boolean arg2,
                boolean arg3, int arg4, int arg5) {
            final JLabel lab = new JLabel("<html><a href=\"" + value + "\">" + value + "</a>");
            return lab;
        }
    });
    String []Registros=new String[5];
    model= (DefaultTableModel) tclientes.getModel();
    model = Dominio.eliminarTabla(model); 
        try {             
              ResultSet rs = this.p.bd.listarDatosMisClientes(valor);
              while(rs.next())
              {
                  Registros[0]= rs.getString("id_mcliente");
                  Registros[1]= rs.getString("nombre");   
                  Registros[2]= rs.getString("telefono");  
                  Registros[3]= rs.getString("correo");  
                  Registros[4]= rs.getString("saldo_favor");  
                  model.addRow(Registros);
              }
              tclientes.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(IngresoMisClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
    cantidad.setText("Cantidad de Clientes: "+tclientes.getRowCount());
   // tclientes = Dominio.centrarDatos(tclientes);
    
     
  }
    

    
