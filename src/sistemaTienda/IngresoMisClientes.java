   private void actualizarCliente(){
      if (validarDatos()){
              int fila= tclientes.getSelectedRow();
              String cod="";
              cod=tclientes.getValueAt(fila, 0).toString(); 
              int n=this.p.bd.modificarMiCliente(nombre.getText(),telefono.getText(),correo.getText(),cod,this);
              if(n>0){
                 JOptionPane.showMessageDialog(null, "Cliente modificado con Exito.");
                 cargar("");
                 principal();
              }
        }
   }
    
    
