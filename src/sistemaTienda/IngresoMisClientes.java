    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
      
        if (tclientes.getSelectedRow()== -1) {
           JOptionPane.showMessageDialog(this,"No ha seleccionado ningún cliente","Advertencia", JOptionPane.WARNING_MESSAGE); //Tipo de mensaje
       }else{
            if (JOptionPane.OK_OPTION == JOptionPane.showConfirmDialog(this, "Desea eliminar al cliente seleccionado junto con sus datos?" )){
              int fila= tclientes.getSelectedRow();
              String cod="";
              cod=tclientes.getValueAt(fila, 0).toString();        
              int n=this.p.bd.eliminarMiCliente(cod);
                if(n>0){
                    JOptionPane.showMessageDialog(this, "Cliente eliminado con Exito.");
                    cargar("");
              
                }else{
                    JOptionPane.showMessageDialog(this, "No se ha podido eliminar al cliente.");
                }   
                principal();
        } 
        }
    }//GEN-LAST:event_eliminarActionPerformed

    
    private void modificarCliente(){
                  
        if (tclientes.getSelectedRow()== -1) {
           JOptionPane.showMessageDialog(this,"No ha seleccionado ningún cliente","Advertencia", JOptionPane.WARNING_MESSAGE); //Tipo de mensaje
       }else{
              cancelar.setEnabled(true);
              nuevo.setEnabled(false);
              guardar.setEnabled(true);
              nombre.requestFocus();
              telefono.setEnabled(true);
              nombre.setEnabled(true);
              correo.setEnabled(true);
              tclientes.setEnabled(false);
              eliminar.setEnabled(false);
              guardar.setEnabled(false);
              modificar.setEnabled(false);
              actualizar.setEnabled(true);
              buscar.setEnabled(false);
              mostrar.setEnabled(false);
              int fila= tclientes.getSelectedRow(); 
              String nom =  tclientes.getValueAt(fila, 1).toString();
              String tel =  tclientes.getValueAt(fila, 2).toString();
              String cor =  tclientes.getValueAt(fila, 3).toString();
              telefono.setText(tel);  
              correo.setText(cor); 
              nombre.setText(nom); 
        }
    }
    
